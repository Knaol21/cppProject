#include <iostream>

int main(){
    char letter = 'A';         // Represents a single character
    int number = 100;          // Represents an integer value
    float decimalNumber = 3.14f; // Represents a single-precision floating-point number
    bool isTrue = true;        // Represents a boolean value (true or false)

    std::cout << "Char: " << letter << "\n";
    std::cout << "Int: " << number << "\n";
    std::cout << "Float: " << decimalNumber << "\n";
    std::cout << "Bool: " << isTrue << "\n";
    return 0;
}
